// Drawing a pattern 
// *
// **
// ****

#include <stdio.h>

int main() {
int rows;
    printf("Enter number of rows \n");
    scanf("%d",& rows);
for(int i=0;i<rows || i == rows;i++){
    for(int j=0; j < i || j == i; j++){
        printf("*");
    }
    printf("\n");
}
    return 0;
}